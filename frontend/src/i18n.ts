import type { LangCode, I18nStore, RenderFn } from './core/types/types';

let currentLang: LangCode = 'zh';
let renderAll: RenderFn | null = null;

export function getCurrentLang(): LangCode {
  return currentLang;
}

export function escapeHtml(str: unknown): string {
  return String(str).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}

export function t(key: string, params?: Record<string, string | number>): string {
  const dict = i18n[currentLang] || i18n['bilingual'];
  let text = dict[key] || i18n['bilingual'][key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}

export function setLang(lang: LangCode): void {
  currentLang = lang;
  // Update toggle buttons
  document.querySelectorAll('#tweaksLangGroup button').forEach(btn => {
    btn.classList.toggle('active', (btn as HTMLElement).dataset.lang === lang);
  });
  // Update data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t((el as HTMLElement).dataset.i18n!);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    (el as HTMLInputElement).placeholder = t((el as HTMLElement).dataset.i18nPlaceholder!);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    (el as HTMLElement).title = t((el as HTMLElement).dataset.i18nTitle!);
  });
  // Re-render dynamic content
  if (renderAll) renderAll();
  // Persist
  localStorage.setItem('nexus-lang', lang);
}

export function initLang(onRenderAll: RenderFn): void {
  renderAll = onRenderAll;
  const saved = localStorage.getItem('nexus-lang') as LangCode | null;
  if (saved && i18n[saved]) {
    currentLang = saved;
  }
}

